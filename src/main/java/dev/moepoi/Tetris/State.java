package dev.moepoi.Tetris;

public class State {
    int row0, col0, row1, col1, row2, col2, row3, col3;

    public State(int row0, int col0, int row1, int col1, int row2,
                 int col2, int row3, int col3) {
        super();
        this.row0 = row0;
        this.col0 = col0;
        this.row1 = row1;
        this.col1 = col1;
        this.row2 = row2;
        this.col2 = col2;
        this.row3 = row3;
        this.col3 = col3;
    }
}
